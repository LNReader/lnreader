const baseUrl = 'https://ficbook.net';

const popularNovels = async (page, { filters }) => {
  let url = baseUrl;

  if (filters?.directions) {
    url += '/popular-fanfics/' + filters.directions;
  } else if (filters?.tags) {
    url += '/tags/' + filters.tags + '?p=' + page;
  } else {
    url += '/' + (filters?.sort || 'fanfiction') + '?p=' + page;
  }

  const result = await fetch(url);
  const body = await result.text();
  const loadedCheerio = cheerio.load(body);

  const novels = [];

  loadedCheerio('article.fanfic-inline').each(function () {
    const novelName = loadedCheerio(this).find('h3 > a').text()?.trim();
    const novelCover = loadedCheerio(this).find('picture > img').attr('src');
    const novelUrl = loadedCheerio(this).find('h3 > a').attr('href');

    if (novelName && novelUrl) {
      novels.push({
        sourceId,
        novelName,
        novelCover: novelCover
          ? novelCover.replace(/covers\/m_|covers\/d_/g, 'covers/')
          : defaultCoverUri,
        novelUrl: novelUrl.replace(/\?.*/g, ''),
      });
    }
  });

  return { novels };
};

const parseNovelAndChapters = async novelUrl => {
  const result = await fetch(baseUrl + novelUrl);
  const body = await result.text();

  const loadedCheerio = cheerio.load(body);

  const novel = {
    sourceId,
    sourceName,
    url: baseUrl + novelUrl,
    novelUrl,
  };

  novel.novelName = (
    loadedCheerio('h1[itemprop="headline"]').text() ||
    loadedCheerio('h1[itemprop="name"]').text()
  ).trim();

  novel.novelCover = loadedCheerio('meta[property="og:image"]').attr('content');
  novel.summary = loadedCheerio('div[itemprop="description"]').text().trim();
  novel.author = loadedCheerio('a[itemprop="author"]').text();

  novel.status =
    loadedCheerio(
      'div.fanfic-main-info > section:nth-child(3) > div:nth-child(3) > span:nth-child(2)',
    ).text() === 'В процессе'
      ? Status.ONGOING
      : Status.COMPLETED;

  const tags = loadedCheerio('div[class="tags"] > a')
    .map((index, element) => loadedCheerio(element).text())
    .get();
  if (tags) {
    novel.genre = tags.join(',');
  }

  if (!novel.novelCover || novel.novelCover?.includes('/design/')) {
    novel.novelCover = defaultCoverUri;
  } else {
    novel.novelCover = novel.novelCover.replace(
      /covers\/m_|covers\/d_/g,
      'covers/',
    );
  }

  const chapters = [];

  if (loadedCheerio('#content').length === 1) {
    const chapterName = loadedCheerio('.title-area > h2').text();
    const releaseDate = loadedCheerio('.part-date > span').attr('title');

    if (chapterName) {
      chapters.push({ chapterName, releaseDate, chapterUrl: novelUrl });
    }
  } else {
    loadedCheerio('li.part').each(function () {
      const chapterName = loadedCheerio(this).find('h3').text();
      const releaseDate = loadedCheerio(this).find('div > span').attr('title');
      const chapterUrl = loadedCheerio(this)
        .find('a:nth-child(1)')
        .attr('href');
      if (chapterName && chapterUrl) {
        chapters.push({ chapterName, releaseDate, chapterUrl });
      }
    });
  }

  novel.chapters = chapters;
  return novel;
};

const parseChapter = async (novelUrl, chapterUrl) => {
  const result = await fetch(baseUrl + chapterUrl);
  const body = await result.text();

  const loadedCheerio = cheerio.load(body);
  let chapterText = '';

  loadedCheerio('#content')
    .html()
    ?.split('\n')
    ?.forEach(line => {
      if (line.trim()) {
        //blank line skip
        if (line.includes('</p>')) {
          //new paragraph
          chapterText += line;
        } else {
          //plaintext
          chapterText += '<p>' + line + '</p>';
        }
      }
    });

  const chapter = {
    sourceId,
    novelUrl,
    chapterUrl,
    chapterName: loadedCheerio('.title-area > h2').text(),
    chapterText,
  };

  return chapter;
};

const searchNovels = async searchTerm => {
  const formData = new FormData();
  formData.append('term', searchTerm);
  formData.append('page', '1');

  const result = await fetch(baseUrl + '/search/fanfic', {
    method: 'POST',
    body: formData,
  });
  const json = await result.json();
  const novels = [];

  json.data?.data?.forEach(novel => {
    const novelName = novel.title.trim();
    const novelUrl = '/readfic/' + novel.slug;
    const novelCover = novel.cover
      ? 'https://images.ficbook.net/fanfic-covers/' + novel.cover
      : defaultCoverUri;

    novels.push({ sourceId, novelName, novelCover, novelUrl });
  });

  return novels;
};

